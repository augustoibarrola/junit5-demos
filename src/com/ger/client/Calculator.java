package com.ger.client;

public class Calculator {
	
	public int addition(int x, int y) {
		return y + x;
	}
	
	public int subtraction(int x, int y) {
		return x - y;
	}
	
	public int multipilication(int x, int y) {
		
		return x * y;
	}
	
	public int division(int x, int y) {
		
		return x / y;
	}

}
